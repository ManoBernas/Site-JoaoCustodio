     */
    public function store(Request $request)
    {
        //
        request()->validate([
            'selectCat' => 'required',
        ]);

        $portfolio = new Portfolio();

        $portfolio->categoria_id = request('selectCat');

        $portfolio->save();

        return redirect('/portfolio')->with('message', 'Produto inserido com sucesso!');
    }

